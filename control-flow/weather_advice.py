weather = input("What's the weather like today? (sunny/rainy/cold): ")

if weather == "sunny": print("Wear a t-shirt and sunglasses.")
elif weather == "rainy": print("Take an umbrella and wear a raincoat.")
elif weather == "cold": print("Wear a warm coat and scarf.")
else: print("Sorry, I didn't understand that. Please enter sunny, rainy, or cold.")

